export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductsByCategory(slug: string): Product[] {
  return products.filter((p) => p.category.includes(slug));
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.isFeatured);
}

export function getNewProducts(): Product[] {
  return products.filter((p) => p.isNew);
}

export function getDiscountedProducts(): Product[] {
  return products.filter((p) => p.discountPercent && p.discountPercent > 0);
}

export function getBudgetProducts(): Product[] {
  return products.filter((p) => p.category.includes("hamyonbop-noutbuklar"));
}

export function getTopProducts(): Product[] {
  return products.filter((p) => p.category.includes("top-noutbuklar"));
}

export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return products
    .filter(
      (p) =>
        p.id !== product.id &&
        (p.brand === product.brand ||
          p.category.some((c) => product.category.includes(c)))
    )
    .slice(0, limit);
}

export function searchProducts(query: string): Product[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.brand.toLowerCase().includes(q) ||
      p.specs.processor.toLowerCase().includes(q) ||
      p.specs.gpu.toLowerCase().includes(q)
  );
}

